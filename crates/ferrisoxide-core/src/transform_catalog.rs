#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize)]
#[serde(rename_all = "snake_case")]
pub enum TransformPackageSupport {
    Supported,
    RejectedDesktopOnly,
    DependencyGated,
    FutureGated,
    NotApplicable,
}

impl TransformPackageSupport {
    pub const fn as_str(self) -> &'static str {
        match self {
            Self::Supported => "supported",
            Self::RejectedDesktopOnly => "rejected_desktop_only",
            Self::DependencyGated => "dependency_gated",
            Self::FutureGated => "future_gated",
            Self::NotApplicable => "not_applicable",
        }
    }

    pub const fn is_supported(self) -> bool {
        matches!(self, Self::Supported)
    }
}

#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize)]
pub struct TransformCatalogParameter {
    pub name: &'static str,
    pub unit: Option<&'static str>,
    pub required: bool,
}

impl TransformCatalogParameter {
    pub const fn required(name: &'static str, unit: &'static str) -> Self {
        Self {
            name,
            unit: Some(unit),
            required: true,
        }
    }

    pub const fn required_unitless(name: &'static str) -> Self {
        Self {
            name,
            unit: None,
            required: true,
        }
    }
}

#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize)]
pub struct TransformCatalogEntry {
    pub name: &'static str,
    pub label: &'static str,
    pub milestone: &'static str,
    pub family: TransformCatalogFamily,
    pub category: TransformCatalogCategory,
    pub config_surface: TransformConfigSurface,
    pub output_kind: TransformCatalogOutputKind,
    pub parameters: &'static [TransformCatalogParameter],
    pub sample_rate_required: bool,
    pub stateful: bool,
    pub causal: bool,
    pub phase_effect: TransformPhaseEffect,
    pub streaming_supported: bool,
    pub offline_only: bool,
    pub runtime_profiles: &'static [TransformRuntimeProfile],
    pub capability_status: TransformCapabilityStatus,
    pub evidence_level: TransformEvidenceLevel,
    pub package_support: TransformPackageSupport,
    pub docs_path: &'static str,
    pub notes: &'static str,
}

impl TransformCatalogEntry {
    pub fn supports_rule_package(self) -> bool {
        self.package_support.is_supported()
    }

    pub fn matches_step_metadata(self, step: &TransformStepMetadata) -> bool {
        self.name == step.name
            && self.category == TransformCatalogCategory::from(step.category)
            && self.output_kind == TransformCatalogOutputKind::from(step.output_channels.kind)
            && self.sample_rate_required == step.sample_rate_required
            && self.stateful == step.stateful
            && self.causal == step.causal
            && self.phase_effect == step.phase_effect
            && self.streaming_supported == step.streaming_supported
            && self.offline_only == step.offline_only
            && self.capability_status == step.capability_status
            && self.evidence_level == step.evidence_level
            && self.runtime_profiles == step.runtime_profiles.as_slice()
    }
}

