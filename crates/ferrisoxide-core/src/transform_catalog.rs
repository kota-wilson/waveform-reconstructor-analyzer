pub fn transform_catalog() -> &'static [TransformCatalogEntry] {
    TRANSFORM_CATALOG
}

pub fn transform_catalog_entry(name: &str) -> Option<&'static TransformCatalogEntry> {
    TRANSFORM_CATALOG.iter().find(|entry| entry.name == name)
}

pub fn implemented_transform_names() -> Vec<&'static str> {
    TRANSFORM_CATALOG
        .iter()
        .filter(|entry| entry.capability_status == TransformCapabilityStatus::Implemented)
        .map(|entry| entry.name)
        .collect()
}

pub fn rule_package_supported_transform_names() -> Vec<&'static str> {
    TRANSFORM_CATALOG
        .iter()
        .filter(|entry| entry.package_support == TransformPackageSupport::Supported)
        .map(|entry| entry.name)
        .collect()
}

